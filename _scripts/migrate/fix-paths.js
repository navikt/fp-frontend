function fixSingleFile(targetFilePath, stringToLookFor, stringToReplaceWith) {
  let targetFileContent = fs.readFileSync(targetFilePath).toString();
  targetFileContent = targetFileContent.replace(`'${stringToLookFor}'`, `'${stringToReplaceWith}'`);
  fs.writeFileSync(targetFilePath, targetFileContent);
}

